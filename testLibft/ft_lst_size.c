#include "libft.h"

int	ft_lst_size(t_list **alst)
{
	int		n;
	t_list	*list;

	if (!(*alst))
		return (0);
	n = 0;
	list = *alst;
	while (list)
	{
		list = list->next;
		n++;
	}
	return (n);
}
