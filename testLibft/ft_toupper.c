#include "libft.h"

int		ft_toupper(int c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	if (c >= 97 && c <= 122)
	{
		ch = ch - 32;
		c = (int)ch;
	}
	return (c);
}
