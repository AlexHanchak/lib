#include "libft.h"

int		ft_isprint(int c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	if (c >= 32 && c <= 126)
	{
		if (ch >= 32 && ch <= 126)
			return (1);
	}
	return (0);
}
